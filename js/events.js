// Generate event card HTML with template string
function createEventCard(event, modalId) {
  return `
            <div class="event-card" data-bs-toggle="modal" data-bs-target="#${modalId}">
                <img src="${event.thumbnail}" alt="${event.title}" />
                <div class="event-overlay">
                    <div class="event-title">${event.title}</div>
                    <div class="event-subtitle">${event.subtitle}</div>
                    <div class="event-date">${event.date}</div>
                </div>
            </div>
        `;
}

// Generate modal HTML with template string
function createModal(event, modalId) {
  // Instagram link HTML if exists
  const instagramLinkHtml = event.post
    ? `
            <p>
                <a class="instagram-link" href="${event.post}" target="_blank">
                    View on Instagram&nbsp;
                </a>
            </p>
        `
    : "";

  // Gallery images HTML
  const galleryImagesHtml = event.images
    .map(
      (imgSrc) => `
            <div class="gallery-image">
                <img src="${imgSrc}" alt="${event.title}" />
            </div>
        `,
    )
    .join("");

  return `
            <div class="modal fade" id="${modalId}" tabindex="-1" aria-labelledby="${modalId}Label" aria-hidden="true">
                <div class="modal-dialog modal-xl">
                    <div class="modal-content">
                        <div class="modal-header">
                            <h5 class="modal-title" id="${modalId}Label">${event.title}</h5>
                            <p>
                                <button
                                    class="btn-close"
                                    type="button"
                                    data-bs-dismiss="modal"
                                    aria-label="Close"
                                ></button>
                            </p>
                        </div>
                        <div class="modal-body">
                            <div class="instagram-link-container">
                                ${instagramLinkHtml}
                            </div>
                            <div class="modal-caption">${event.description}</div>
                            <div class="image-gallery">
                                ${galleryImagesHtml}
                            </div>
                        </div>
                    </div>
                </div>
            </div>
        `;
}

// Generate event cards and modals
function generateEvents() {
  const eventsGrid = document.getElementById("events-grid");
  const modalsContainer = document.getElementById("modals-container");

  let cardsHtml = "";
  let modalsHtml = "";

  eventsData.forEach((event, index) => {
    const modalId = `eventModal${index}`;

    // Generate event card HTML
    cardsHtml += createEventCard(event, modalId);

    // Generate modal HTML
    modalsHtml += createModal(event, modalId);
  });

  // Append all HTML at once for better performance
  eventsGrid.innerHTML = cardsHtml;
  modalsContainer.innerHTML = modalsHtml;
}

// Initialize when DOM is loaded
document.addEventListener("DOMContentLoaded", function () {
  generateEvents();

  // Add hover effects for gallery images
  document.addEventListener("mouseover", function (e) {
    if (e.target.closest(".gallery-image")) {
      e.target.closest(".gallery-image").style.transform = "scale(1.02)";
    }
  });

  document.addEventListener("mouseout", function (e) {
    if (e.target.closest(".gallery-image")) {
      e.target.closest(".gallery-image").style.transform = "scale(1)";
    }
  });
});
